package School;

public class Person {
    private String name;
    private int age;
    private String gender;
    private String phone;
    private String email;

    public Person(String name, int age, String gender, String phone, String email) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.phone = phone;
        this.email = email;
    }
}
